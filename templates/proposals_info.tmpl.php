<h1><?=__('Modalidades') ?></h1>

<h2><?=__('Conferencias') ?></h2>
<p><?=__('Una conferencia aborda un tema determinado (técnico, filosófico, de
opinión, etc). En ella el ponente presenta el tema a desarrollar ante
un auditorio. Las conferencias seran impartidas en el auditorio principal.
Se les dedica tipicamente sesiones de 1 hora (algunas 2 horas).') ?></p>

<h2><?=__('Charlas Informales') ?></h2>
<p><?=__('Una conferencia informal esta orientada a aquellas personas que
quieran presentar algun tema, propuesta,  presentación, software, etc.
Estas no se rigen por el formato típico ponente-auditorio, es mas bien una
ponencia o reunión entre entusiastas o interesados en el tema donde el
ponente presenta su tema en una ambiente mucho mas relajado e interactivo
con los asistentes. A estas se les dedica tipicamente menos de 2 horas.') ?></p>

<h2><?=__('Talleres') ?></h2>
<p><?=__('Un taller aborda a profundidad un tema técnico determinado,
dedicándole una sesión de hasta cuatro horas, es didáctico y práctico por
naturaleza. Los talleres serán impartidos laboratorios de cómputo, de modo
que todos los asistentes puedan ir aprendiendo y poniendo en práctica de
inmediato lo que el ponente está presentando.') ?></p>

<h2><?=__('Tutoriales') ?></h2>
<p><?=__('Un tutorial aborda un tema técnico determinado, dedicándole por
lo menos una sesión de dos horas. Los temas muchas veces serán teóricos.
Los tutoriales seran impartidos en salones o laboratorios de computo donde
el ponente ira presentando a los asistentes el tema tratado.
Los tutoriales van a un ritmo mucho más ágil que los talleres.') ?></p>

<h2><?=__('Clases Magistrales') ?></h2>
<p><?=__('Las clases magistrales serán impartidas por profesionales
de la tecnología de la información de alto nivel, líderes de opinión o
desarrolladores destacados de la comunidad del software libre.') ?>
<br />
<?=__('En las conferencias magistrales se expondrán los temas o tópicos
que vayan de acuerdo al hilo central del evento.') ?>
</p>

